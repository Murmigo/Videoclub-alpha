    private Pelicula buscarPelicula(int id)
    {
        Pelicula peli = new Pelicula();
        int contador =0;
        boolean encontrado = false;
        while(encontrado == false && contador< listaPeliculas2.size())
        {
            if(id == listaPeliculas2.get(contador).id)
                encontrado = true;
            else
                contador++;
        }
        if(encontrado){
           peli = listaPeliculas2.get(contador);
        }
    
        return peli;
    }
public void mostrarLista(ArrayList<Prestamos> listaPrestamosloc){
//crear el objeto que almacena el título de las películas
        DefaultListModel titulo = new DefaultListModel();
        //bucle for que recorre la lista de las películas
        for(int i=0; i<listaPrestamosloc.size(); i++){ 
            titulo.addElement(buscarPelicula(listaPrestamosloc.get(i).idPelicula).titulo); 
        }
        //muestro en el jList el título de las películas
        jList1.setModel(titulo);
    }
